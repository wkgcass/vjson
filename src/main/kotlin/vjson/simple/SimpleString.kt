package vjson.simple

import vjson.JSON
import vjson.JSON.String.Companion.stringify
import vjson.Stringifier
import vjson.cs.LineCol
import vjson.pl.ScriptifyContext

class SimpleString /*#ifndef KOTLIN_NATIVE {{ */ @JvmOverloads/*}}*/ constructor(
  private val str: String,
  private val lineCol: LineCol = LineCol.EMPTY
) : JSON.Instance<String>, JSON.String {
  private var stringified: String? = null

  override fun toJavaObject(): String {
    return str
  }

  override fun stringify(): String {
    if (stringified == null) {
      stringified = stringify(str)
    }
    return stringified!!
  }

  fun stringify(stringOptions: Stringifier.StringOptions?): String {
    if (stringOptions == null) return stringify()
    return stringify(str, stringOptions)
  }

  override fun pretty(): String {
    return stringify()
  }

  override fun stringify(builder: StringBuilder, sfr: Stringifier) {
    builder.append(stringify(sfr.stringOptions()))
  }

  override fun scriptify(builder: StringBuilder, ctx: ScriptifyContext) {
    builder.append(ScriptifyContext.scriptifyString(str))
  }

  override fun lineCol(): LineCol {
    return lineCol
  }

  override fun toString(): String {
    return "String($str)"
  }

  override fun equals(other: Any?): Boolean {
    if (this === other) return true
    if (other !is JSON.String) return false
    return toJavaObject() == other.toJavaObject()
  }

  override fun hashCode(): Int {
    return str.hashCode()
  }
}
